/**
 * Web UI launcher via docmd.
 *
 * docmd-search and docmd are completely independent tools.  When the user
 * runs `docmd-search ./docs --ui`, docmd-search:
 *
 *   1. Indexes the directory (builds .docmd-search/ batches) — its own job.
 *   2. Writes a self-contained docmd.config.json into .docmd-search/_ui/.
 *   3. Spawns `npx @docmd/core dev` pointed at that config.
 *
 * The generated config tells docmd to use its own plugin-search with
 * `semantic: true` AND an `indexDir` pointing at the already-built index.
 * This way docmd's plugin-search skips re-indexing entirely — it just reads
 * the batches that docmd-search already produced and serves the semantic
 * client bundle.  No dependency loop: docmd doesn't call back into
 * docmd-search; it just reads files from disk.
 *
 * If docmd is not installed, the error message tells the user how to install
 * it.  docmd is an *optional* runtime dep for --ui mode only.
 */

import { writeFile, mkdir, copyFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { join, resolve } from 'node:path';
import { spawn, execFileSync } from 'node:child_process';

