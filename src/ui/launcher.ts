/* ── Launch Function ───────────────────────────────────────── */

export interface LaunchOptions {
  /** Root directory being searched. */
  rootDir: string;
  /** Index output directory (.docmd-search). */
  indexDir: string;
  /** Port to run on (0 = auto). */
  port?: number;
  /** Open browser automatically. */
  open?: boolean;
  /** Verbose output. */
  verbose?: boolean;
}

/**
 * Launch the docmd web UI for docmd-search.
 *
 * Creates a staging area inside .docmd-search/ with:
 * - docmd.config.json (auto-generated)
 * - custom.css (magenta branding)
 *
 * Then spawns `npx @docmd/core dev` pointing at the target directory.
 */
export async function launchWebUI(options: LaunchOptions): Promise<{
  port: number;
  close: () => void;
}> {
  const { rootDir, indexDir, port = 0, open = true, verbose = false } = options;

  // ── Create staging area ──────────────────────────────

  const stagingDir = join(indexDir, '_ui');
  await mkdir(stagingDir, { recursive: true });

  // Write custom CSS first (config references its path)
  const cssContent = generateCustomCSS();
  await writeFile(join(stagingDir, 'custom.css'), cssContent, 'utf-8');

  // Write docmd config
  const configContent = generateDocmdConfig(rootDir, indexDir);
  await writeFile(join(stagingDir, 'docmd.config.json'), configContent, 'utf-8');

  // Copy navigation.json from index into rootDir so docmd picks it up
  // docmd reads navigation.json from the docs source directory automatically
  const indexNavFile = join(indexDir, 'navigation.json');
  const rootNavFile = join(rootDir, 'navigation.json');
  if (existsSync(indexNavFile) && !existsSync(rootNavFile)) {
    try {
      await copyFile(indexNavFile, rootNavFile);
      if (verbose) {
        console.log(`   ${A.dim}navigation → ${rootNavFile}${A.reset}`);
      }
    } catch {
      // Non-fatal — docmd will auto-generate navigation from files
    }
  }

  // Create index.md with docmd-search template if it doesn't exist
  // This ensures docmd has something to render even in non-markdown directories
  const indexMdPath = join(rootDir, 'index.md');
  if (!existsSync(indexMdPath)) {
    const indexMdContent = `---
title: docmd-search
template: docmd-search
---

# Search Results

Use the search bar above to find content in this directory.

Results will appear here as you type.
`;
    try {
      await writeFile(indexMdPath, indexMdContent, 'utf-8');
      if (verbose) {
        console.log(`   ${A.dim}index.md → ${indexMdPath}${A.reset}`);
      }
    } catch {
      // Non-fatal — docmd may still work with existing files
    }
  }

  if (verbose) {
    console.log(`   ${A.dim}staging → ${stagingDir}${A.reset}`);
  }

  // ── Spawn docmd ──────────────────────────────────────

  return new Promise((resolvePromise, reject) => {
    // Resolve the docmd binary:
    // 1. Local node_modules/.bin/docmd (from the rootDir or CWD)
    // 2. Globally installed docmd in PATH
    // 3. Fall back to: npx @docmd/core
    const resolvedBinary = resolveDocmdBinary(rootDir);
    let command: string;
    let args: string[];

    if (resolvedBinary) {
      command = resolvedBinary;
      args = ['dev', '--config', join(stagingDir, 'docmd.config.json')];
      if (verbose) {
        console.log(`   ${A.dim}using docmd at: ${resolvedBinary}${A.reset}`);
      }
    } else {
      // Fall back to npx
      command = 'npx';
      args = ['-y', '@docmd/core', 'dev', '--config', join(stagingDir, 'docmd.config.json')];
      if (verbose) {
        console.log(`   ${A.dim}falling back to: npx @docmd/core${A.reset}`);
      }
    }

    if (port > 0) {
      args.push('--port', String(port));
    }

    const child = spawn(command, args, {
      cwd: rootDir,
      stdio: ['ignore', 'pipe', 'pipe'],
      env: {
        ...process.env,
        DOCMD_SEARCH_INDEX: indexDir,
      },
    });

    let resolvedPort = port || 3000;
    let resolved = false;

    // Always parse stdout for port detection; optionally forward to terminal
    if (child.stdout) {
      child.stdout.on('data', (data: Buffer) => {
        const line = data.toString();
        if (verbose) process.stdout.write(line);

        // Detect port from docmd output
        const portMatch = line.match(/localhost:(\d+)/);
        if (portMatch && !resolved) {
          resolvedPort = parseInt(portMatch[1], 10);
          resolved = true;

          console.log(`   ${A.magenta}◆${A.reset} Web UI ready`);
          console.log(`   ${A.dim}http://localhost:${resolvedPort}${A.reset}`);
          console.log('');

          // Open browser
          if (open) {
            openBrowser(`http://localhost:${resolvedPort}`);
          }

          resolvePromise({
            port: resolvedPort,
            close: () => {
              child.kill('SIGTERM');
            },
          });
        }
      });
    }

    if (child.stderr && verbose) {
      child.stderr.on('data', (data: Buffer) => process.stderr.write(data));
    }

    // Handle errors
    child.on('error', (err) => {
      if (err.message.includes('ENOENT') || err.message.includes('npx')) {
        console.log('');
        console.log(`   ${A.red}✗${A.reset} Could not launch web UI`);
        console.log('');
        console.log(`   ${A.dim}Web UI requires @docmd/core. Install it with:${A.reset}`);
        console.log(`     npm install -g @docmd/core`);
        console.log('');
        console.log(`   ${A.dim}Or use the TUI search: docmd-search ./docs (without --ui)${A.reset}`);
        console.log('');
      }
      reject(err);
    });

    child.on('exit', (code) => {
      if (!resolved) {
        if (code !== 0) {
          console.log('');
          console.log(`   ${A.red}✗${A.reset} docmd exited with code ${code}`);
          console.log('');
          console.log(`   ${A.dim}Web UI requires @docmd/core. Install it with:${A.reset}`);
          console.log(`     npm install -g @docmd/core`);
          console.log('');
          console.log(`   ${A.dim}Or use the TUI search: docmd-search ./docs (without --ui)${A.reset}`);
          console.log('');
        }
        reject(new Error(`docmd exited with code ${code}`));
      }
    });

    // Timeout: if docmd doesn't start in 30s, fallback
    setTimeout(() => {
      if (!resolved) {
        resolved = true;
        resolvePromise({
          port: resolvedPort,
          close: () => {
            child.kill('SIGTERM');
          },
        });
      }
    }, 30000);
  });
}

/* ── Browser Opener ────────────────────────────────────────── */

function openBrowser(url: string): void {
  try {
    const cmd = process.platform === 'darwin' ? 'open'
      : process.platform === 'win32' ? 'start'
      : 'xdg-open';
    spawn(cmd, [url], { detached: true, stdio: 'ignore' }).unref();
  } catch {
    // Silently fail — user can open manually
  }
}

/* ── Binary Resolver ───────────────────────────────────────── */

/**
 * Resolve the docmd CLI binary.
 *
 * Priority:
 *  1. Local node_modules/.bin/docmd (rootDir or CWD)
 *  2. docmd in PATH (global install)
 *  3. Returns null → caller falls back to npx
 */
function resolveDocmdBinary(rootDir: string): string | null {
  const absRootDir = resolve(rootDir);

  // Walk up from rootDir and CWD looking for node_modules/.bin/docmd
  const searchRoots = [absRootDir, resolve(process.cwd())];
  const visited = new Set<string>();

  for (const start of searchRoots) {
    let dir = start;
    while (true) {
      if (visited.has(dir)) break;
      visited.add(dir);

      const candidate = join(dir, 'node_modules', '.bin', 'docmd');
      if (existsSync(candidate)) return candidate;

      const parent = join(dir, '..');
      if (parent === dir) break; // filesystem root
      dir = parent;
    }
  }

  // Check global PATH — use execFileSync (no shell) for safety.
  try {
    const whichCmd = process.platform === 'win32' ? 'where' : 'which';
    const result = execFileSync(whichCmd, ['docmd'], { stdio: 'pipe' })
      .toString()
      .trim()
      .split('\n')[0]
      .trim();
    if (result && existsSync(result)) return result;
  } catch {
    // Not in PATH
  }

  return null;
}