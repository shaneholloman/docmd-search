/* ── Config Generation ─────────────────────────────────────── */

/**
 * Generate a docmd.config.json tailored for docmd-search's web UI.
 *
 * Key architecture points:
 *  - docmd-search has ALREADY built the semantic index at this point
 *  - We tell docmd's plugin-search to use `semantic: true` with `indexDir`
 *    pointing at the pre-built index — plugin-search will skip re-indexing
 *  - docmd just serves the UI; no dependency loop back to docmd-search
 *  - Custom CSS applies the magenta docmd-search branding
 */
function generateDocmdConfig(rootDir: string, indexDir: string): string {
  const absRootDir = resolve(rootDir).replace(/\\/g, '/');
  const absIndexDir = resolve(indexDir).replace(/\\/g, '/');
  const absCssFile = resolve(indexDir, '_ui', 'custom.css').replace(/\\/g, '/');

  // JSON.stringify escapes quotes, backticks, and other metacharacters
  // that could break out of the template literal (CWE-94 defence).
  const safeSrc = JSON.stringify(absRootDir).slice(1, -1);
  const safeOut = JSON.stringify(absIndexDir + '/_site').slice(1, -1);
  const safeCss = JSON.stringify(absCssFile).slice(1, -1);
  const safeIdx = JSON.stringify(absIndexDir).slice(1, -1);

  return `// Auto-generated by docmd-search — do not edit manually.
// docmd-search has already built the semantic index; docmd just serves the UI.

export default {
  src: '${safeSrc}',
  out: '${safeOut}',
  title: 'docmd-search',
  description: 'Offline semantic search',

  theme: {
    customCss: ['${safeCss}'],
  },

  plugins: {
    // Semantic search with pre-built index — plugin-search skips indexing
    // and just reads from indexDir. No callback to docmd-search package.
    search: {
      semantic: true,
      showConfidence: true,
      indexDir: '${safeIdx}',
    },
  },

  // Disable features not needed for the search UI
  analytics: false,
  seo: false,
  sitemap: false,
};
`;
}

